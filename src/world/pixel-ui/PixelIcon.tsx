const OUTLINE_COLOR = "#1b1b1b";
const DETAIL_COLOR = "#f6efe3";

function PixelGlyph({ name }: { name: PixelIconName }): JSX.Element {
  const rows = GRIDS[name];
  const cells: JSX.Element[] = [];
  for (let y = 0; y < rows.length; y++) {
    const row = rows[y] ?? "";
    for (let x = 0; x < row.length; x++) {
      const ch = row[x];
      if (ch === ".") continue;
      const fill = ch === "o" ? OUTLINE_COLOR : ch === "w" ? DETAIL_COLOR : "currentColor";
      cells.push(<rect key={`${y}-${x}`} x={x} y={y} width={1} height={1} fill={fill} />);
    }
  }
  return (
    <svg viewBox="0 0 32 32" width="100%" height="100%" style={{ shapeRendering: "crispEdges", display: "block" }}>
      {cells}
    </svg>
  );
}

type PixelIconProps = HTMLAttributes<HTMLSpanElement> & {
  name: PixelIconName;
  label?: string;
  /** Explicit pixel size (used by the icon matrix). Defaults to the 1.1em CSS sizing. */
  size?: number;
};

export function PixelIcon({ name, label, size, className = "", style, ...props }: PixelIconProps): JSX.Element {
  return (
    <span
      className={`pixel-icon pixel-icon--${name} ${className}`}
      role={label ? "img" : undefined}
      aria-label={label}
      aria-hidden={label ? undefined : true}
      style={size ? { width: size, height: size, ...style } : style}
      {...props}
    >
      <PixelGlyph name={name} />
    </span>
  );
}
